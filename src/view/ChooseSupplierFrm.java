    public ChooseSupplierFrm(Tour tour, Schedule unsavedSchedule ,Service selectedService) {

        initComponents();
        this.selectedService = selectedService;
        this.tour = tour;
        this.unsavedSchedule = unsavedSchedule;

        this.cbPlace.removeAllItems();
        for (PlaceSchedule placeSchedule: unsavedSchedule.getListPlaceSchedule()) {
            cbPlace.addItem(placeSchedule);
        }

        ServiceSupplierDAO serviceSupplierDAO = new ServiceSupplierDAO();
        List<ServiceSupplier> serviceSuppliers = serviceSupplierDAO.getAllServiceSupplierByService(selectedService);
        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        model.setRowCount(0);
        for (ServiceSupplier serviceSupplier: serviceSuppliers) {
            Supplier supplier = serviceSupplier.getSupplier();
            model.addRow(new Object[] {
                    serviceSupplier.getId(),
                    supplier.getName(),
                    supplier.getEmail(),
                    supplier.getTel(),
                    serviceSupplier.getType(),
                    serviceSupplier.getPrice(),
                    supplier.getNote()
            });
        }
    }

