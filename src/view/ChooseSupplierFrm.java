    private void tblResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultMouseClicked
        // TODO add your handling code here:
        ServiceSupplier selectedServiceSupplier = new ServiceSupplier();
        int row = tblResult.getSelectedRow();
        selectedServiceSupplier.setId((int) tblResult.getValueAt(row, 0));
        selectedServiceSupplier.setType((String) tblResult.getValueAt(row, 4));
        selectedServiceSupplier.setPrice((int) tblResult.getValueAt(row, 5));
        Supplier supplier = new Supplier();
        supplier.setName((String) tblResult.getValueAt(row, 1));
        supplier.setEmail((String) tblResult.getValueAt(row, 2));
        supplier.setTel((String) tblResult.getValueAt(row, 3));
        supplier.setNote((String) tblResult.getValueAt(row, 6));
        selectedServiceSupplier.setSupplier(supplier);
        selectedServiceSupplier.setService(selectedService);

        String quantity = JOptionPane.showInputDialog("Enter the quantity:");
        if (quantity != null && !quantity.isEmpty()) {
            int quantityInt = Integer.parseInt(quantity);
            PlaceSchedule selectedPlaceSchedule = (PlaceSchedule) cbPlace.getSelectedItem();
            PlaceSchedule existingPlaceSchedule = unsavedSchedule.getListPlaceSchedule().stream()
                    .filter(placeSchedule -> placeSchedule.equals(selectedPlaceSchedule))
                    .findFirst()
                    .orElse(null);

            ServiceSchedule serviceSchedule = new ServiceSchedule();
            serviceSchedule.setServiceSupplier(selectedServiceSupplier);
            serviceSchedule.setQuantity(quantityInt);
            serviceSchedule.setPrice(selectedServiceSupplier.getPrice());
            existingPlaceSchedule.getListServiceSchedule().add(serviceSchedule);

            (new AddServicesFrm(tour, unsavedSchedule)).setVisible(true);
            this.dispose();
        }
        else {
            JOptionPane.showMessageDialog(this, "Please enter the quantity");
        }
    }//GEN-LAST:event_tblResultMouseClicked

