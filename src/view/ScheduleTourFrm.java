    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Tour newTour = new Tour();
        newTour.setCreator(creator);
        newTour.setName(this.txtName.getText());
        newTour.setDeparturePlace(this.txtDeparturePlace.getText());
        newTour.setDestination(this.txtDestination.getText());
        newTour.setDescription(this.txtDescription.getText());
        
        TourDAO tourDAO = new TourDAO();
        int success = tourDAO.addTour(newTour);
        if(success != -1) {
            JOptionPane.showMessageDialog(this, "Successful");
            DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
            model.setRowCount(0);
            Object[] row = {
                success,
                newTour.getName(),
                newTour.getDeparturePlace(),
                newTour.getDestination()
            };
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "Try again!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultMouseClicked
        // TODO add your handling code here:
        Tour tour = new Tour();
        int row = tblResult.getSelectedRow();
        tour.setId((int) tblResult.getValueAt(row, 0));
        tour.setName((String) tblResult.getValueAt(row, 1));
        tour.setDeparturePlace((String) tblResult.getValueAt(row, 2));
        tour.setDestination((String) tblResult.getValueAt(row, 3));

        (new AddScheduleFrm(tour, new Schedule())).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_tblResultMouseClicked

