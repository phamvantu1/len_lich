    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchKeyword = txtNameSearch.getText();
        TourDAO tourDAO = new TourDAO();
        List<Tour> searchResult = tourDAO.searchTour(searchKeyword); 
        
        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        model.setRowCount(0); // 

        for (Tour tour : searchResult) {
            Object[] row = {
                tour.getId(),
                tour.getName(),
                tour.getDeparturePlace(),
                tour.getDestination()
            };
            model.addRow(row);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

