    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        unsavedSchedule.setDepartureDay(new Date(txtDepartureDay.getText()));
        unsavedSchedule.setTicketPrice(Float.parseFloat(txtTicketPrice.getText()));
        ScheduleDAO scheduleDAO = new ScheduleDAO();
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to save this schedule?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if(scheduleDAO.addSchedule(tour, unsavedSchedule)) {
                JOptionPane.showMessageDialog(this, "Successfully saved schedule");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to save schedule");
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

