    public AddScheduleFrm(Tour tour, Schedule unsavedSchedule) {
        initComponents();
        this.tour = tour;
        this.unsavedSchedule = unsavedSchedule;
        this.lblIdTour.setText(String.valueOf(tour.getId()));
        this.lblNameTour.setText(tour.getName());

        DefaultTableModel modelPlace = (DefaultTableModel) tblPlaceInfo.getModel();
        modelPlace.setRowCount(0);
        DefaultTableModel modelService = (DefaultTableModel) tblServiceInfo.getModel();
        modelService.setRowCount(0);

        if(unsavedSchedule.getListPlaceSchedule() != null) {
            for(PlaceSchedule placeSchedule : unsavedSchedule.getListPlaceSchedule()){
                modelPlace.addRow(new Object[]{
                        placeSchedule.getPlace().getName(),
                        placeSchedule.getPlace().getLocation(),
                        placeSchedule.getHourVisit(),
                        placeSchedule.getPlace().getDescription()
                });
                if(placeSchedule.getListServiceSchedule() != null) {
                    for(ServiceSchedule serviceSchedule : placeSchedule.getListServiceSchedule()){
                        modelService.addRow(new Object[]{
                                serviceSchedule.getServiceSupplier().getService().getName(),
                                serviceSchedule.getServiceSupplier().getType(),
                                serviceSchedule.getServiceSupplier().getPrice(),
                                serviceSchedule.getServiceSupplier().getSupplier().getName(),
                                placeSchedule.getPlace().getName(),
                                serviceSchedule.getQuantity()
                        });
                    }
                }

            }
        }

    }

